// Como exibir textos em maiúsculo ou minúsculo em JavaScript

let mensagem = "Estou estudando os fundamentos do JavaScript!";

// Exibir o texto em maiúsculo
let mensagemMaiuscula = mensagem.toUpperCase();
console.log("\n" + mensagemMaiuscula); // Output: "ESTOU ESTUDANDO OS FUNDAMENTOS DO JAVASCRIPT!"

// Exibir o texto em minúsculo
let mensagemMinuscula = mensagem.toLowerCase();
console.log("\n" + mensagemMinuscula); // Output: "estou estudando os fundamentos do javascript!"