/*
===========================================================
🚀 ALTERNATIVAS PARA RODAR JAVASCRIPT
===========================================================

JavaScript pode ser executado em diferentes ambientes.

1️⃣ 🌐 No Navegador (via HTML)
   - Conectado usando <script>;
   - Manipula o DOM;
   - Cria interatividade;
   - Responde a eventos; e
   - Roda diretamente no browser.

2️⃣ 🖥 Node.js
   - Executa JS fora do navegador;
   - Permite criar servidores;
   - Criar APIs;
   - Trabalhar com banco de dados;
   - Rodar automações; e
   - Comando: node arquivo.js.

3️⃣ 🧪 Playground Online (ex: playground.dev);
   - Não precisa instalar nada;
   - Ideal para testes rápidos;
   - Ótimo para iniciantes.

===========================================================
📌 RESUMO FINAL:
===========================================================

🌐 Navegador → Interatividade web;
🖥 Node.js → Backend e servidores;
🧪 Playground → Testes rápidos online.

JavaScript é versátil porque roda
em múltiplos ambientes 🚀 */