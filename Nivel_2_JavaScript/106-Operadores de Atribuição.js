// Operadores de Atribuição

let valor;
valor = 10;
console.log(`O valor é de: ${valor}`)
let incremento = ++valor;
console.log(`O valor com incremento é de: ${incremento}`);
let decremento1 = --valor;
console.log(`O valor com decremento, vira ele mesmo: ${decremento1}`);
let decremento2 = --valor;
console.log(`O valor com decremento vira: ${decremento2}`)